//Check if a Number is Even or Odd.
public class Basic_if_else_even_odd {

    public static void main(String args[]){
        int a = 24;

        if(a % 2 == 0){
            System.out.println("Given number is even Number " +a);
        }
        else
            System.out.println("Given number is odd Number " +a);
    }
}
